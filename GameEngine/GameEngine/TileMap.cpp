TileMap::TileMap()
{
	limitPoint = TextureManager::LoadTexture("Assests/preto.png");
	Point = TextureManager::LoadTexture("Assests/branco.png");
	offPoint= TextureManager::LoadTexture("Assests/vermelho.png");

	LoadMap(lvl1);
	src.x = 0;
	src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = 0;
	dest.y = 0;

}

TileMap::~TileMap()
{

}

void TileMap::LoadMap(int array[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = array[row][column];
		}
	}
}

void TileMap::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(limitPoint, src, dest);
				break;
			case 1:
				TextureManager::Draw(Point, src, dest);
				break;
			case 2:
				TextureManager::Draw(offPoint, src, dest);
				break;
			default:
				break;
			}
		}
	}
}
